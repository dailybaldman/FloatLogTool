package FloatLogTool;

public class IndexData {
    
    int index;
	String data;

	public IndexData(int index, String data) {
		this.index = index;
		this.data = data;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getIndex() {
		return index;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getData() {
		return data;
	}
    
}
